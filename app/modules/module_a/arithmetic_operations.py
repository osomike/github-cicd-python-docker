"""
This is a module to perform arithmetic operations
"""

def sum_values(value_a: float, value_b: float):
    """
        A method to add two values. Returns the sum.

    Args:
        value_a (float): value 1
        value_b (float): value 2

    Returns:
        float: the sum of both values.
    """
    return value_a + value_b

def multiply_values(value_a: float, value_b: float):
    """
        A method to multiply two values. Returns the multiplication.

    Args:
        value_a (float): value 1
        value_b (float): value 2

    Returns:
        float: the multiplication of both values.
    """
    return value_a * value_b
